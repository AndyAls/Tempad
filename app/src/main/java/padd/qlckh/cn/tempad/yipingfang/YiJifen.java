package padd.qlckh.cn.tempad.yipingfang;

public class YiJifen {
    private String boli;
    private String dianchi;
    private String jinshu;
    private String qita;
    private String suliao;
    private String zhiban;

    public String getSuliao() {
        return this.suliao;
    }

    public void setSuliao(String str) {
        this.suliao = str;
    }

    public String getZhiban() {
        return this.zhiban;
    }

    public void setZhiban(String str) {
        this.zhiban = str;
    }

    public String getBoli() {
        return this.boli;
    }

    public void setBoli(String str) {
        this.boli = str;
    }

    public String getDianchi() {
        return this.dianchi;
    }

    public void setDianchi(String str) {
        this.dianchi = str;
    }

    public String getJinshu() {
        return this.jinshu;
    }

    public void setJinshu(String str) {
        this.jinshu = str;
    }

    public String getQita() {
        return this.qita;
    }

    public void setQita(String str) {
        this.qita = str;
    }
}